
public class Actor {

	int actorID;
	String firstName;
	String lastName;
	
	public int getActorID() {
		return actorID;
	}
	
	public void setActorID(int id) {
		this.actorID = id;
	}
	
	public String getFirstName() {
		return firstName;
	}
	
	public void setFirstName(String name) {
		this.firstName = name;
	}
	
	public String getLastName() {
		return lastName;
	}
	
	public void setLastName(String surname) {
		this.lastName = surname;
	}
	
}
